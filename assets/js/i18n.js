// Current language
let currentLang = localStorage.getItem('language') || 'en';

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    loadLanguage(currentLang);
    setupLanguageButtons();
});

function loadLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('language', lang);

    // Update active button
    document.querySelectorAll('.language-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.dataset.lang === lang) {
            btn.classList.add('active');
        }
    });

    // Update meta tags
    document.getElementById('meta-description').content = translations[lang]['meta.description'];
    document.getElementById('og-title').content = translations[lang]['og.title'];
    document.getElementById('og-description').content = translations[lang]['og.description'];
    document.title = lang === 'en'
        ? 'Tirto Lesmana Lutvi - Full Stack Developer & UI/UX Designer'
        : 'Tirto Lesmana Lutvi - Full Stack Developer & UI/UX Designer';

    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                // Handle HTML content
                element.innerHTML = translations[lang][key];
            }
        }
    });

    // Reinitialize AOS animations
    if (typeof AOS !== 'undefined') {
        AOS.refresh();
    }
}

function setupLanguageButtons() {
    document.querySelectorAll('.language-btn').forEach(button => {
        button.addEventListener('click', () => {
            const lang = button.dataset.lang;
            loadLanguage(lang);

            // Add a small animation effect
            button.classList.add('language-change-animation');
            setTimeout(() => {
                button.classList.remove('language-change-animation');
            }, 300);
        });
    });
}